// ─── Membership ─────────────────────────────────────────────────────────────────

export interface JoinMembershipParams {
  userId: string;
  ward: string;
  skills?: string[];
  hasVehicle?: boolean;
}

export interface MembershipResponse {
  membership: {
    memberNumber: string;
    tier: NBKCMembershipTier;
    ward: string;
    joinedAt: Date;
    perks: string[];
  };
  greenScore: number;
  greenBengaluruScore: number;
}

/**
 * Join NBKC as a citizen member.
 * Creates membership + green score profile.
 */
export async function joinCivicCorps(params: JoinMembershipParams): Promise<MembershipResponse> {
  const { userId, ward, skills = [], hasVehicle = false } = params;

  // Check existing membership
  const existing = await NBKCMembership.findOne({ userId: new (require('mongoose').Types.ObjectId)(userId) });
  if (existing) {
    throw new Error('Already a member of NBKC');
  }

  // Create membership
  const membership = await NBKCMembership.create({
    userId: new (require('mongoose').Types.ObjectId)(userId),
    memberNumber: '', // auto-generated by pre-save
    tier: 'citizen',
    ward,
    skills,
    hasVehicle,
    stickerIssued: false,
    isActive: true,
    totalCivicHours: 0,
    missionsCompleted: 0,
    civicReportsVerified: 0,
  });

  // Create green score profile
  const greenProfile = await GreenScoreProfile.create({
    userId: new (require('mongoose').Types.ObjectId)(userId),
  });

  logger.info('[NBKC] New member joined', {
    userId,
    memberNumber: membership.memberNumber,
    ward,
  });

  return {
    membership: {
      memberNumber: membership.memberNumber,
      tier: membership.tier,
      ward: membership.ward,
      joinedAt: membership.joinedAt,
      perks: membership.getPerks(),
    },
    greenScore: greenProfile.greenScore,
    greenBengaluruScore: greenProfile.greenBengaluruScore,
  };
}

/**
 * Get a user's NBKC membership status.
 */
export async function getCivicCorpsStatus(userId: string): Promise<{
  isMember: boolean;
  membership?: {
    memberNumber: string;
    tier: NBKCMembershipTier;
    ward: string;
    joinedAt: Date;
    totalCivicHours: number;
    missionsCompleted: number;
    stickerIssued: boolean;
    perks: string[];
    isEligibleForSticker: boolean;
  };
  greenScore: number;
  greenBengaluruScore: number;
}> {
  const { Types } = require('mongoose');
  const oid = new Types.ObjectId(userId);

  const [membership, greenProfile] = await Promise.all([
    NBKCMembership.findOne({ userId: oid }),
    GreenScoreProfile.findOne({ userId: oid }),
  ]);

  if (!membership) {
    return {
      isMember: false,
      greenScore: greenProfile?.greenScore ?? 0,
      greenBengaluruScore: greenProfile?.greenBengaluruScore ?? 0,
    };
  }

  return {
    isMember: true,
    membership: {
      memberNumber: membership.memberNumber,
      tier: membership.tier,
      ward: membership.ward,
      joinedAt: membership.joinedAt,
      totalCivicHours: membership.totalCivicHours,
      missionsCompleted: membership.missionsCompleted,
      stickerIssued: membership.stickerIssued,
      perks: membership.getPerks(),
      isEligibleForSticker: membership.isEligibleForSticker(),
    },
    greenScore: greenProfile?.greenScore ?? 0,
    greenBengaluruScore: greenProfile?.greenBengaluruScore ?? 0,
  };
}

// ─── Missions ─────────────────────────────────────────────────────────────────

export interface MissionListParams {
  ward?: string;
  category?: string;
  limit?: number;
  page?: number;
}

/**
 * List available civic missions.
 */
export async function listMissions(params: MissionListParams = {}): Promise<{
  missions: ICivicMission[];
  total: number;
  page: number;
}> {
  const { ward, category, limit = 20, page = 1 } = params;
  const skip = (page - 1) * limit;

  const filter: Record<string, unknown> = {
    status: 'active',
    scheduledAt: { $gte: new Date() },
  };
  if (ward) filter.ward = ward;
  if (category) filter.category = category;

  const [missions, total] = await Promise.all([
    CivicMission.find(filter).sort({ scheduledAt: 1 }).skip(skip).limit(limit),
    CivicMission.countDocuments(filter),
  ]);

  return { missions, total, page };
}


/**
 * Get NBKC ward leaderboard.
 */
export async function getWardLeaderboard(ward: string, limit = 20) {
  const { Types } = require('mongoose');
  const members = await NBKCMembership.aggregate([
    { $match: { ward, isActive: true } },
    { $sort: { totalCivicHours: -1 } },
    { $limit: limit },
    {
      $lookup: {
        from: 'green_score_profiles',
        localField: 'userId',
        foreignField: 'userId',
        as: 'greenProfile',
      },
    },
    {
      $project: {
        userId: 1,
        memberNumber: 1,
        tier: 1,
        totalCivicHours: 1,
        missionsCompleted: 1,
        greenBengaluruScore: { $arrayElemAt: ['$greenProfile.greenBengaluruScore', 0] },
      },
    },
  ]);

  return members.map((m, i) => ({ rank: i + 1, ...m }));
}


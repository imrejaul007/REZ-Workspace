# ============================================================================
# Enums
# ============================================================================

class AgentType(str, Enum):
    MARKET = "market"
    FUNDAMENTAL = "fundamental"
    TECHNICAL = "technical"
    SENTIMENT = "sentiment"
    RISK = "risk"
    MACRO = "macro"
    NEWS = "news"
    EARNINGS = "earnings"


class AgentStatus(str, Enum):
    IDLE = "idle"
    WORKING = "working"
    COMPLETED = "completed"
    ERROR = "error"


class AnalysisPriority(str, Enum):
    LOW = "low"
    NORMAL = "normal"
    HIGH = "high"
    CRITICAL = "critical"


class AssetClass(str, Enum):
    STOCKS = "stocks"
    BONDS = "bonds"
    COMMODITIES = "commodities"
    CRYPTO = "crypto"
    FOREX = "forex"
    MIXED = "mixed"


# ============================================================================
# Pydantic Models - Agents
# ============================================================================

class Agent(BaseModel):
    """Represents a specialized AI agent."""
    id: str
    name: str
    type: AgentType
    description: str
    capabilities: List[str] = []
    status: AgentStatus = AgentStatus.IDLE
    current_task: Optional[str] = None
    tasks_completed: int = 0
    avg_response_time_ms: int = 500
    accuracy_score: float = 0.75


class AgentTask(BaseModel):
    """A task assigned to an agent."""
    task_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    agent_type: AgentType
    symbol: Optional[str] = None
    query: str
    priority: AnalysisPriority = AnalysisPriority.NORMAL
    status: AgentStatus = AgentStatus.IDLE
    created_at: datetime = Field(default_factory=datetime.utcnow)
    completed_at: Optional[datetime] = None


class AgentInsight(BaseModel):
    """Insight generated by an agent."""
    agent_type: AgentType
    agent_name: str
    insight_type: str
    content: str
    confidence: float = Field(..., ge=0.0, le=1.0)
    supporting_data: Dict[str, Any] = {}
    timestamp: datetime = Field(default_factory=datetime.utcnow)


# ============================================================================
# Pydantic Models - Analysis
# ============================================================================

class TechnicalIndicators(BaseModel):
    rsi: float = 50.0
    macd: float = 0.0
    macd_signal: float = 0.0
    macd_histogram: float = 0.0
    sma_20: float = 0.0
    sma_50: float = 0.0
    sma_200: float = 0.0
    bollinger_upper: float = 0.0
    bollinger_lower: float = 0.0
    atr: float = 0.0
    adx: float = 0.0


class FundamentalMetrics(BaseModel):
    pe_ratio: float = 0.0
    eps: float = 0.0
    revenue: float = 0.0
    revenue_growth: float = 0.0
    earnings_growth: float = 0.0
    profit_margin: float = 0.0
    debt_to_equity: float = 0.0
    current_ratio: float = 0.0
    quick_ratio: float = 0.0
    roe: float = 0.0
    roa: float = 0.0


class SentimentData(BaseModel):
    overall_sentiment: str = "neutral"
    sentiment_score: float = 0.5
    news_count: int = 0
    social_mentions: int = 0
    analyst_rating: str = "neutral"
    institutional_ownership: float = 0.0
    short_interest: float = 0.0


class MacroIndicators(BaseModel):
    interest_rate: float = 0.0
    inflation_rate: float = 0.0
    gdp_growth: float = 0.0
    unemployment: float = 0.0
    consumer_confidence: float = 0.0
    dollar_index: float = 0.0
    treasury_yield: float = 0.0


class MultiAgentAnalysis(BaseModel):
    """Complete analysis from all agents."""
    analysis_id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    symbol: str
    asset_class: AssetClass = AssetClass.STOCKS

    # Agent insights
    market_insight: Optional[AgentInsight] = None
    fundamental_insight: Optional[AgentInsight] = None
    technical_insight: Optional[AgentInsight] = None
    sentiment_insight: Optional[AgentInsight] = None
    risk_insight: Optional[AgentInsight] = None
    macro_insight: Optional[AgentInsight] = None
    news_insight: Optional[AgentInsight] = None
    earnings_insight: Optional[AgentInsight] = None

    # Combined metrics
    technical_indicators: Optional[TechnicalIndicators] = None
    fundamental_metrics: Optional[FundamentalMetrics] = None
    sentiment_data: Optional[SentimentData] = None
    macro_indicators: Optional[MacroIndicators] = None

    # Summary
    overall_score: float = 0.5
    recommendation: str = "HOLD"
    confidence: float = 0.5
    key_themes: List[str] = []
    risks: List[str] = []
    opportunities: List[str] = []

    # Metadata
    created_at: datetime = Field(default_factory=datetime.utcnow)
    processing_time_ms: int = 0

    class Config:
        from_attributes = True



@app.post("/api/analyze", response_model=MultiAgentAnalysis, status_code=201)
async def analyze(request: AnalysisRequest):
    """Run multi-agent analysis on a symbol."""
    start_time = datetime.utcnow()

    # Select agents
    selected_agents = AGENTS
    if request.include_agents:
        selected_agents = [a for a in AGENTS if a.type in request.include_agents]

    # Generate insights from each agent
    analysis = MultiAgentAnalysis(
        symbol=request.symbol.upper(),
        asset_class=request.asset_class,
    )

    for agent in selected_agents:
        insight = generate_agent_insight(agent, request.symbol)
        agent.tasks_completed += 1

        if agent.type == AgentType.MARKET:
            analysis.market_insight = insight
        elif agent.type == AgentType.FUNDAMENTAL:
            analysis.fundamental_insight = insight
        elif agent.type == AgentType.TECHNICAL:
            analysis.technical_insight = insight
        elif agent.type == AgentType.SENTIMENT:
            analysis.sentiment_insight = insight
        elif agent.type == AgentType.RISK:
            analysis.risk_insight = insight
        elif agent.type == AgentType.MACRO:
            analysis.macro_insight = insight
        elif agent.type == AgentType.NEWS:
            analysis.news_insight = insight
        elif agent.type == AgentType.EARNINGS:
            analysis.earnings_insight = insight

    # Generate combined metrics
    if AgentType.TECHNICAL in (request.include_agents or [a.type for a in AGENTS]):
        analysis.technical_indicators = generate_technical_indicators()

    if AgentType.FUNDAMENTAL in (request.include_agents or [a.type for a in AGENTS]):
        analysis.fundamental_metrics = generate_fundamental_metrics()

    if AgentType.SENTIMENT in (request.include_agents or [a.type for a in AGENTS]):
        analysis.sentiment_data = SentimentData(
            overall_sentiment="positive",
            sentiment_score=0.65,
            news_count=random.randint(10, 50),
            social_mentions=random.randint(1000, 50000),
            analyst_rating="buy",
            institutional_ownership=random.uniform(50, 85),
            short_interest=random.uniform(2, 10),
        )

    if AgentType.MACRO in (request.include_agents or [a.type for a in AGENTS]):
        analysis.macro_indicators = MacroIndicators(
            interest_rate=5.25,
            inflation_rate=3.2,
            gdp_growth=2.5,
            unemployment=3.8,
            consumer_confidence=105,
            dollar_index=104.5,
            treasury_yield=4.35,
        )

    # Calculate overall score and recommendation
    confidences = [insight.confidence for insight in [
        analysis.market_insight, analysis.fundamental_insight,
        analysis.technical_insight, analysis.sentiment_insight,
        analysis.risk_insight
    ] if insight]

    analysis.overall_score = sum(confidences) / len(confidences) if confidences else 0.5
    analysis.confidence = analysis.overall_score

    if analysis.overall_score > 0.75:
        analysis.recommendation = "STRONG_BUY"
    elif analysis.overall_score > 0.60:
        analysis.recommendation = "BUY"
    elif analysis.overall_score > 0.45:
        analysis.recommendation = "HOLD"
    elif analysis.overall_score > 0.30:
        analysis.recommendation = "SELL"
    else:
        analysis.recommendation = "STRONG_SELL"

    # Key themes and risks
    analysis.key_themes = ["AI-driven growth", "Strong balance sheet", "Market leadership"]
    analysis.risks = ["Interest rate sensitivity", "Competition in sector", "Market volatility"]
    analysis.opportunities = ["Expansion into new markets", "Product innovation", "M&A potential"]

    end_time = datetime.utcnow()
    analysis.processing_time_ms = int((end_time - start_time).total_seconds() * 1000)

    analyses_db[analysis.analysis_id] = analysis
    return analysis


@app.post("/api/quick-analyze", response_model=Dict[str, Any])
async def quick_analyze(request: QuickAnalysisRequest):
    """Quick analysis from top 4 agents."""
    top_agents = sorted(AGENTS, key=lambda a: a.accuracy_score, reverse=True)[:4]

    insights = []
    for agent in top_agents:
        insight = generate_agent_insight(agent, request.symbol)
        insights.append(insight)

    avg_confidence = sum(i.confidence for i in insights) / len(insights)

    return {
        "symbol": request.symbol.upper(),
        "insights": [i.model_dump() for i in insights],
        "overall_score": avg_confidence,
        "quick_recommendation": "BUY" if avg_confidence > 0.65 else "HOLD",
        "timestamp": datetime.utcnow().isoformat(),
    }


@app.get("/api/analyses", response_model=List[MultiAgentAnalysis])
async def list_analyses(
    symbol: Optional[str] = None,
    limit: int = Query(20, ge=1, le=100),
):
    """List recent analyses."""
    results = list(analyses_db.values())

    if symbol:
        results = [a for a in results if a.symbol == symbol.upper()]

    results.sort(key=lambda a: a.created_at, reverse=True)
    return results[:limit]


@app.get("/api/analyses/{analysis_id}", response_model=MultiAgentAnalysis)
async def get_analysis(analysis_id: str):
    """Get a specific analysis."""
    if analysis_id not in analyses_db:
        raise HTTPException(status_code=404, detail="Analysis not found")
    return analyses_db[analysis_id]



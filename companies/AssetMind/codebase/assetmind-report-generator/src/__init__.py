@app.post("/generate", response_model=GeneratedReport)
async def generate_report(request: ReportRequest):
    """Generate an investment report."""

    report_id = f"rpt-{datetime.now().timestamp()}"

    if request.format == ReportFormat.HTML:
        content = generate_html_report(request)
        content_base64 = base64.b64encode(content.encode()).decode()
        file_size = len(content.encode())
    elif request.format == ReportFormat.EXCEL:
        content = generate_excel_content(request)
        content_base64 = base64.b64encode(json.dumps(content).encode()).decode()
        file_size = len(json.dumps(content).encode())
    elif request.format == ReportFormat.PDF:
        content = generate_pdf_content(request)
        content_base64 = base64.b64encode(content).decode()
        file_size = len(content)
    else:
        content = {"report": request.dict()}
        content_base64 = base64.b64encode(json.dumps(content).encode()).decode()
        file_size = len(json.dumps(content).encode())

    return GeneratedReport(
        report_id=report_id,
        title=request.title,
        report_type=request.report_type.value,
        format=request.format.value,
        created_at=datetime.now().isoformat(),
        file_size=file_size,
        content_base64=content_base64,
        download_url=f"/reports/{report_id}/download"
    )


@app.post("/generate/portfolio-summary")
async def generate_portfolio_summary(
    portfolio_name: str,
    total_value: float,
    day_pnl: float,
    positions: List[Dict]
):
    """Generate portfolio summary report."""
    sections = [
        Section(
            title="Portfolio Overview",
            content=f"Total portfolio value: ${total_value:,.2f}",
            data={"Portfolio": portfolio_name, "Total Value": f"${total_value:,.2f}"}
        ),
        Section(
            title="Daily Performance",
            content=f"Day P&L: ${day_pnl:,.2f} ({day_pnl/total_value*100:.2f}%)",
            data={"P&L": f"${day_pnl:,.2f}", "Return": f"{day_pnl/total_value*100:.2f}%"}
        ),
        Section(
            title="Top Holdings",
            content="Current positions",
            data=positions[:10] if len(positions) > 10 else positions
        )
    ]

    request = ReportRequest(
        report_type=ReportType.PORTFOLIO_SUMMARY,
        title=f"Portfolio Summary: {portfolio_name}",
        subtitle=datetime.now().strftime("%B %d, %Y"),
        sections=sections,
        format=ReportFormat.HTML
    )

    return await generate_report(request)



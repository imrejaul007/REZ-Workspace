export const getAgentById = (id: string): Agent | undefined => {
  return agents.find(agent => agent.id === id);
};

export const getAgentsByCategory = (categorySlug: string): Agent[] => {
  return agents.filter(agent => agent.categorySlug === categorySlug);
};

export const getCategoryBySlug = (slug: string): Category | undefined => {
  return categories.find(category => category.slug === slug);
};

export const searchAgents = (query: string): Agent[] => {
  const lowerQuery = query.toLowerCase();
  return agents.filter(
    agent =>
      agent.name.toLowerCase().includes(lowerQuery) ||
      agent.description.toLowerCase().includes(lowerQuery) ||
      agent.capabilities.some(cap => cap.toLowerCase().includes(lowerQuery))
  );
};

export const getFeaturedAgents = (): Agent[] => {
  return [...agents]
    .sort((a, b) => b.installCount - a.installCount)
    .slice(0, 6);
};

export const getTopRatedAgents = (): Agent[] => {
  return [...agents]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, 6);
};

export const getFreeAgents = (): Agent[] => {
  return agents.filter(agent => agent.price === 0);
};

export const getPopularCategories = (): Category[] => {
  return [...categories].sort((a, b) => b.agentCount - a.agentCount).slice(0, 6);
};